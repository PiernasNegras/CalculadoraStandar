    private void jButton23ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton23ActionPerformed
        
        num2=Integer.parseInt(Pantalla.getText());

        //genero un switch que evalua el contenido de la variable signo
        //y actua en consecuencia
        switch(signo){
            //case en el que el signo sea "+"
            case "+":
                //le digo que me muestre por pantalla la suma de mis variables
                //num1 y num2
                Pantalla.setText(Integer.toString(num1+num2));
                break;
                            //case en el que el signo sea "+"
            case "-":
                //le digo que me muestre por pantalla la resta de mis variables
                //num1 y num2
                Pantalla.setText(Integer.toString(num1-num2));
                break;
                        //case en el que el signo sea "+"
            case "/":
                //le digo que me muestre por pantalla la division de mis variables
                //num1 y num2
                Pantalla.setText(Integer.toString(num1/num2));
                break;
                        //case en el que el signo sea "+"
            case "*":
                //le digo que me muestre por pantalla la multiplicacion de mis variables
                //num1 y num2
                Pantalla.setText(Integer.toString(num1*num2));
                break;
        }
        
        
    }//GEN-LAST:event_jButton23ActionPerformed

